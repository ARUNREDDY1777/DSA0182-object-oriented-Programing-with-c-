#include <iostream> 
using namespace std; 
  
int main() 
{ 
    int n1, n2; 
    n1 = 10; 
    n2 = 3; 
    cout<< "n1 + n2= " << (n1 + n2) << endl; 
    cout << "n1 -n2 = " << (n1 - n2) << endl;  
    cout << "n1 *n2 = " << (n1 * n2) << endl;  
    cout << "n1 /n2 = " << (n1 / n2) << endl;  
    cout << "n1% n2 = " << (n1 % n2) << endl; 
  
    return 0; 
}
